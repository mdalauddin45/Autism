	<h1 style="color:#71b7e6">What Causes Autism Spectrum Disorder?</h1>
	<p>
		Autism Spectrum Disorder (ASD) is a complex developmental condition characterized by challenges with social interaction, communication, and repetitive behaviors. The exact causes of ASD are not fully understood, but research suggests a combination of genetic, environmental, neurological factors and biological factors.
	</p>

	<h2 style="color:#71b7e6"> Genetic Factors</h2>

	<b style="color:cyan;">Genetic Mutations and Variations: </b><br>
	
		Numerous studies have identified that certain genetic mutations and variations are linked to ASD. These include both inherited genes and spontaneous mutations that occur during conception or early development.<br><br>


	<h4 style="color:#71b7e6">Family History: </h4>

	<ul type="square">
		<li>assigned male at birth</li>
		<li>having an autistic sibling or parent</li>
		<li>a chromosomal condition like fragile X syndrome</li>
		
	</ul>

	<b style="color:cyan;">Gene-Environment Interactions: </b><br>
	Some genes may make individuals more susceptible to environmental factors that could trigger ASD.
	<br><br>

	<h2 style="color:#71b7e6"> Prenatal Factors</h2>

	<b style="color:cyan;">Advanced Parental Age: </b> <br>
	Older parents, particularly fathers, have a higher risk of having children with ASD. This may be due to genetic mutations that increase with age.<br><br>
	

	<b style="color:cyan;">Maternal Health: </b> <br>
	Conditions such as diabetes, obesity, and infections during pregnancy can increase the risk of ASD. Certain medications taken during pregnancy, like valproic acid, are also associated with higher ASD risk.<br><br>
	

	<b style="color:cyan;">Exposure to Toxins: </b><br>
	
		Exposure to environmental toxins such as pesticides, heavy metals, and air pollution during pregnancy has been linked to an increased risk of ASD.<br><br>
	

	<h2 style="color:#71b7e6">Perinatal and Neonatal Factors</h2>
	<b style="color:cyan;">Premature Birth: </b><br>
	
		Babies born preterm or with low birth weight are at a higher risk for developmental disorders, including ASD.<br><br>
	
	<h4 style="color:#71b7e6">Birth Complications: </h4>

		Complications during delivery that result in oxygen deprivation or other forms of neonatal distress may contribute to the development of ASD.<br><br>
	
	<b style="color:cyan;">Neurotransmitter Imbalances:</b><br>
	
		Imbalances in neurotransmitters, which are chemicals that transmit signals in the brain, such as serotonin and dopamine, have been implicated in ASD.<br><br>
	
	<h2 style="color:#71b7e6">Neurological Factors</h2>
	<b style="color:cyan;">Brain Development: </b><br>
	Differences in brain structure and function are often observed in individuals with ASD. These differences can include abnormal growth of the brain, differences in connectivity between neurons, and variations in brain regions responsible for social interaction and communication.
