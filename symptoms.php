			<h4 style="color:cyan;">Highly Restricted, Fixated Interests:</h4>

			<ul type="square">
				<li>Intense focus on specific topics or objects.</li>
				<li>Unusual preoccupations that are abnormal in intensity or focus.</li>


			</ul>

			<h4 style="color:cyan;">Hyper- or Hypo-re-activity to Sensory Input:</h4>

			<ul type="square">
				<li>Unusual reactions to sensory stimuli (e.g., sounds, textures, lights, smells, tastes).
				</li>
				<li>Over-sensitivity or under-sensitivity to pain, temperature, or other sensory experiences.</li>

			</ul>

			<h3 style="color:#71b7e6">Additional Considerations</h3>
			<h4 style="color:cyan;">Language and Communication Delays:</h4>

			<ul type="square">
				<li>Delayed speech development or lack of speech (nonverbal).</li>
				<li>Difficulty understanding or using spoken language.</li>


			</ul>

			<h4 style="color:cyan;">Cognitive and Learning Differences:</h4>

			<ul type="square">
				<li>Variability in cognitive abilities, with strengths in some areas and challenges in others.</li>
				<li>Difficulty with abstract thinking and problem-solving.</li>

			</ul>

			<h4 style="color:cyan;">Emotional and Behavioral Responses:</h4>

			<ul type="square">
				<li>Unusual emotional responses or difficulty regulating emotions.</li>
				<li>Potential for tantrums or meltdowns due to sensory overload or changes in routine.</li>

			</ul>

