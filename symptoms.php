			<h1 style="color:#71b7e6">What Are the Symptoms of Autism Spectrum Disorder?</h1>

			<p>Autism Spectrum Disorder (ASD) encompasses a range of symptoms that can vary widely in severity and impact. These symptoms generally appear in early childhood and can be grouped into two main categories: challenges with social communication and interaction, and restricted or repetitive behaviors and interests.</p>

			<h2 style="color:#71b7e6">Social Communication and Interaction</h2>

			<h4 style="color:cyan;">Difficulty with Social Reciprocity: </h4>

			<ul type="square">
				<li>Limited back-and-forth conversations.</li>
				<li>Reduced sharing of interests or emotions with others.</li>
				<li>Failure to initiate or respond to social interactions appropriately.</li>
			</ul>

			<h4 style="color:cyan;">Challenges with Nonverbal Communication:</h4>

			<ul type="square">
				<li>Limited use of gestures (e.g., pointing, waving).</li>
				<li>Difficulty maintaining eye contact.</li>
				<li>Atypical facial expressions, tone of voice, and body language.</li>
				<li>expressing their thoughts and feelings</li>
			</ul>

			<h4 style="color:cyan;">Difficulty Understanding Relationships:</h4>

			<ul type="square">
				<li>Struggle to make friends or engage in age-appropriate play.</li>
				<li>Difficulty adjusting behavior to suit various social contexts.</li>
				<li>
					Limited understanding of social cues and norms.
				</li>

			</ul>

			<h3 style="color:#71b7e6">Restricted or Repetitive Behaviors and Interests</h3>
			<h4 style="color:cyan;">Stereotyped or Repetitive Motor Movements:</h4>

			<ul type="square">
				<li>Hand-flapping, rocking, spinning, or other repetitive movements./li>
				<li>Repetitive use of objects (e.g., lining up toys, flipping objects,finger flicking,coin spinning,other repeated actions).
				</li>

			</ul>

			<h4 style="color:cyan;">Insistence on Sameness:</h4>

			<ul type="square">
				<li>Strong preference for routines and rituals.</li>
				<li>Extreme distress at small changes in routines or environment.</li>

			</ul>

